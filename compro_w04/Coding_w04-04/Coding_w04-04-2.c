#include <stdio.h>

int main() {

    // กำหนดคะแนนเป็น float
    float score = 90.5;

    // พิมพ์คะแนนโดยใช้ %f สำหรับ float
    printf("Score: %.1f\n", score);

    return 0;
}

/*ทำไมต้องเลือก Data Type ที่ถูกต้อง
    จะทำให้โค้ดไม่มีการผิดพลาด
    จะทำให้ได้ข้อมูลที่ครบถ้วน
อธิปาย และสรุปผล
    คำสั่ง int ไม่รองรับข้อมูลที่เป็นทศนิยม
    คำสั่ง float รองรับข้อมูลที่เป็นทศนิยม
*/