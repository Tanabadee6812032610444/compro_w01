#include <stdio.h> //บอกคอมไพเลอร์ให้ใช้ไลบรารี stdio.h สำหรับการทำงานกับ I/O
//ผิดตรงไม่มีเครื่องหมาย<>
int main() { //ประกาศฟังก์ชันหลัก
    int a = 10; //ระบุตัวแปร a และกำหนดค่าเริ่มต้นเป็น 10
    //ผิดตรงไม่มีเครื่องหมาย;
    printf("a = %d\n", a); //แสดงค่าของตัวแปร a ออกหน้าจอ
    //ผิดตรงไม่มีเครื่องหมาย;
    return 0; //คืนค่า 0 เพื่อบอกว่าการทำงานสำเร็จ
    //ผิดตรงไม่มีเครื่องหมาย;
}
